void help(const char *path)
{
    const char *pname = strrchr(path, '/');
    if (!pname) pname = path;
    else pname++;

    printf("Usage: %s <options>\n", pname);
    printf("\nWhere <file> is either the gmmidi.xml or gmdrums.xml file.\n");

    printf("\nOptions:\n");
    printf("  --mt32\t\t\tPrint the MT-32 instrument conficturation.\n");
    printf("  --gm2\t\t\t\tPrint the GM2 instrument conficturation.\n");
    printf("  --gs\t\t\t\tPrint the GS instrument conficturation.\n");
    printf("  --xg\t\t\t\tPrint the XG instrument conficturation.\n");
    printf("  --all\t\t\t\tPrint all of the above.\n");

    printf("\n");
    exit(-1);
}

int main(int argc, char **argv)
{
   const char *pname = strrchr(argv[0], '/');
   const char *env;
   int mode = GM;
   int b;

   if (argc < 2) help(argv[0]);

   env = getCommandLineOption(argc, argv, "--all");
   if (env) mode |= GM2|GS|XG|MT32;
   else
   {
      env = getCommandLineOption(argc, argv, "--gm2");
      if (env) mode |= GM2;

      env = getCommandLineOption(argc, argv, "--gs");
      if (env) mode |= GS;

      env = getCommandLineOption(argc, argv, "--xg");
      if (env) mode |= XG;

      env = getCommandLineOption(argc, argv, "--mt32");
      if (env) mode |= MT32;
   }

   if (pname) pname++;
   printf("<?xml version=\"1.0\"?>\n\n");
   printf("<!--\n * Auto generated by %s\n", pname);
   printf(" *\n * Instrument list for: ");
   if (mode & GM) printf("GM ");
   if (mode & GS) printf("GS ");
   if (mode & MT32) printf("MT-32 ");
   if (mode & GM2) printf("GM 2.0 ");
   if (mode & XG) printf("XG");
   printf("\n-->\n");
   printf("<aeonwave>\n\n");
   printf(" <midi name=\"AeonWave\">\n\n");

   if (mode & GM) print_GM();
   if (mode & GS) print_GS();
   if (mode & MT32) print_MT32();
   if (mode & GM2) print_GM2();
   if (mode & XG) print_XG();

   printf(" </midi>\n\n");
   printf("</aeonwave>\n");

   return 0;
}

