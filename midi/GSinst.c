static char filename[256];
char *lowercase(const char *name)
{
   int i, j ,len = strlen(name);
   if (len > 256) len = 255;

   j = 1;
   filename[0] = tolower(name[0]);
   for (i=1; i<len; ++i)
   {
      if (name[i] != '(' && name[i] != ')')
      {
         if (name[i] == ' ') filename[j] = '-';
         else filename[j] = tolower(name[i]);
         j++;
      }
   }
   filename[j] = 0;
   return filename;
}

